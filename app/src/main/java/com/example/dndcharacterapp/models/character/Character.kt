data class Character(
    val _id: String,
    val name: String? = null,
    val level: Int? = null,
    val inspiration: Boolean = false,
    val background: String? = null,
    val race: Race? = null,
    val alignment: Alignment? = null,
    val hitPoints: HitPoints? = null,
    val hitDie: Array<HitDie>? = null,
    val deathSaves: DeathSaves? = null,
    val temporaryHitPoints: Int? = null,
    val exhaustion: Int? = null,
    val armorClass: ArmorClass? = null,
    val classes: Array<ClassItem>? = null,
    val experiencePoints: Int? = null,
    val stats: Array<Stat>? = null,
    val skillProficiencies: Array<SkillProficiency>? = null,
    val languages: Array<Language>? = null,
    val otherProficiencies: Array<Proficiency>? = null,
    val equipment: Array<Equipment>? = null,
