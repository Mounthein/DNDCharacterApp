    val passiveWisdom: Int? = null,
    val initiative: Int? = null,
    val speed: Int? = null,
    val proficiencyBonus: Int? = null,
    val savingThrows: Array<String>? = null,
    val personalityTrait: String? = null,
    val ideals: String? = null,
    val bonds: String? = null,
    val flaws: String? = null,
    val characterAppearance: String? = null,
    val characterBackstory: String? = null,
    val alliesOrganizations: String? = null,
    val symbol: String? = null
